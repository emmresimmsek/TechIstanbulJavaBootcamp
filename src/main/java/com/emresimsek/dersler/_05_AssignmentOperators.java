package com.emresimsek.dersler;

public class _05_AssignmentOperators {
    public static void main(String[] args) {

        //(=) Sağdaki bir değer (veya ifade) sonucunu soldaki değişkene atar.
        int a = 10; // x değişkenine 10 atanır
        System.out.println(a); // Çıktı: 10

        //(+=) Topla ve ata
        int b = 11;
        System.out.println(b+=5);

        //(*=) Çarp ve ata
        int c = 20;
        System.out.println(c*=2);

        //(-=) Çıkar ve ata
        int d = 10;
        System.out.println(d-=5);

        //(/=) Böl ve ata
        int e = 10;
        System.out.println(e/=2);

        //(%=) Mod al ve kalanı ata.
        int f = 10;
        System.out.println(f%=3);













    }
}
