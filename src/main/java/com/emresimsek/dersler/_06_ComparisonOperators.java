package com.emresimsek.dersler;

public class _06_ComparisonOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        // == Operatörü
        System.out.println( a == b );

        // != Operatörü
        System.out.println( a != b );

        // Büyük mü? (>)
        System.out.println( a > b );

        // Küçük mü? (<)
        System.out.println( a < b );

        // Büyük ya da eşit mi? (>=)
        System.out.println( a >= b );

        // Küçük ya da eşit mi? (<=)
        System.out.println( a <= b);


















    }
}
