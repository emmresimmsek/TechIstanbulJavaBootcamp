package com.emresimsek.dersler;

public class _02_PrimitiveDataTypes {
    public static void main(String[] args) {

        // Veri türü, bir değişkenin hangi türde bir veriyi saklayabileceğini belirleyen bir özelliktir.
        // Değişkenler bir veri tipi ile tanımlanır.
        // 1- Primitive (İlkel) Veri Tipleri
        // Primitive (ilkel) veri tipleri null olamaz çünkü doğrudan bir değer tutarlar.
        byte b = 127;
        short s = 32767;
        int i = 2147483647; // Varsayılan olarak (default) int kullanılır.
        long l = 9223372036854775807L; // Eğer bir sayı int sınırlarını aşan büyük bir değer içeriyorsa, L kullanmanız zorunludur.

        float f = 3.14f;  // Kullanırken sonuna "f" eklemek gerekir.
        double d = 3.14159265358979323846;
        boolean bool = true;
        char c = 'a'; // Tek bir karakteri saklar ve tek tırnak içinde yazılır.  Unicode karakter tablosunu destekler.



        // 2- Referans (Reference) Veri Tipleri
        // null, yalnızca referans (reference) veri tipleri için geçerlidir.
        // a: String
        // b: Arrays
        // c: Classes
        // d: Interfac
        // e: Wrapper Classes









    }
}
