package com.emresimsek.dersler;

public class _09_EscapeCharacters {
    public static void main(String[] args) {

        //Tek tırnak işareti ekleme.
        System.out.println("Ben \'Java\' öğreniyorum.");

        //Çift tırnak işareti ekleme.
        System.out.println("Ben \"Java\" öğreniyorum.");

        //Ters eğik çizgi.
        System.out.println("Ben \\Java öğreniyorum.");

        //Satır başı.
        System.out.println("Ben \rJava öğreniyorum.");

        //Tab boşluğu ekleme.
        System.out.println("Ben \tJava öğreniyorum.");

        //Yeni satır oluşturur.
        System.out.println("Ben \nJava öğreniyorum.");

        //Bir geri adım yapar.
        System.out.println("Ben \bJava öğreniyorum.");



    }
}
